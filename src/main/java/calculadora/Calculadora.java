    //método que se actva al pulsar el botón +
    private void botonSumarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonSumarActionPerformed
        if (activado == true) {
            //se guarda en la variable primerNumero el valor de la cadenaNumeros
            primerNumero = Double.parseDouble(cadenaNumeros);
            //se muestra en la parte de arriba (en el label de arriba) lo que había en la cadenaNumeros
            // con un +
            etiquetaMuestra.setText(cadenaNumeros + " + ");
            //se borra la cadenaNumeros
            cadenaNumeros = "";
            //se determina la operación como sumar
            operacion = "sumar";

            //al poner activado=false evitamos que se active el evento al pulsar el botón +
            activado = false;
            punto = true;
        }
    }//GEN-LAST:event_botonSumarActionPerformed

    //método que se activa al pulsar el botón =
    private void botonIgualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonIgualActionPerformed
        double segundoNumero;
        //con el switch se decide que operación se hará según operación
        switch (operacion) {
            case "sumar":
                segundoNumero = Double.parseDouble(cadenaNumeros);
                resultado = primerNumero + segundoNumero;
                etiquetaNumeros.setText(String.valueOf(resultado));
                cadenaNumeros = String.valueOf(resultado);
                operacion = "nula";
                break;
            case "restar":
                segundoNumero = Double.parseDouble(cadenaNumeros);
                resultado = primerNumero - segundoNumero;
                etiquetaNumeros.setText(String.valueOf(resultado));
                cadenaNumeros = String.valueOf(resultado);
                operacion = "nula";
                break;
            case "multiplicar":
                segundoNumero = Double.parseDouble(cadenaNumeros);
                resultado = primerNumero * segundoNumero;
                etiquetaNumeros.setText(String.valueOf(resultado));
                cadenaNumeros = String.valueOf(resultado);
                operacion = "nula";
                break;
            case "dividir":
                segundoNumero = Double.parseDouble(cadenaNumeros);
                if (segundoNumero == 0) {
                    //si el divisor es 0 se indica que no se puede
                    etiquetaNumeros.setText("No se puede");
                } else {
                    resultado = primerNumero / segundoNumero;
                    etiquetaNumeros.setText(String.valueOf(resultado));
                    cadenaNumeros = String.valueOf(resultado);
                    operacion = "nula";
                }
                break;
        }
        etiquetaMuestra.setText("");
        activado = true;
    }//GEN-LAST:event_botonIgualActionPerformed

