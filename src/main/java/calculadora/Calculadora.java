    //método que activa un evento al pulsar el botón .
    private void botonPuntoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonPuntoActionPerformed
        if (punto == true) {
            if (cadenaNumeros.equals("")) {
                //si no hay ningún nº se pone automático 0.
                cadenaNumeros = "0.";
            } else {
                //si hay un nº se pone el punto a la derecha de este
                cadenaNumeros += ".";
            }
            etiquetaNumeros.setText(cadenaNumeros);
            //indicamos que punto = false para que solo se pueda poner un punto por nº
            punto = false;
        }
    }//GEN-LAST:event_botonPuntoActionPerformed

    //método que activa un evento al pulsar el botón +/-
    private void botonSignoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonSignoActionPerformed
        if (cadenaNumeros.charAt(0) != '-') {
            //si la posición 0 de cadenaNumero es diferente de -
            if (!cadenaNumeros.equals("0")) {
                //si el nº de la cadenaNumeros es diferente a 0 se cambia el signo
                cadenaNumeros = "-" + cadenaNumeros;
            }            
        } else {
            //sino, se quita la posición 0 de la cadenaNumeros al decir que empieza por la posición 1
            cadenaNumeros = cadenaNumeros.substring(1, cadenaNumeros.length());
        }
        //se muestra en el label
        etiquetaNumeros.setText(cadenaNumeros);
    }//GEN-LAST:event_botonSignoActionPerformed

    //método que activa un evento al pulsar el botón CE
    private void botonCEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonCEActionPerformed
        int tamanio = cadenaNumeros.length(); //tamaño de la cadenaNumeros
        if (tamanio > 0) {
            //si el tamaño es mayor que 0
            if (tamanio == 1) {
                //si el tamaño es igual a 1 se pone un 0
                cadenaNumeros = "0";
            } else {
                //si no es igual a 1 se quita el último caracter de cadenaNumeros al indicar que cadenaNumeros
                //termina en la posición length() - 1
                cadenaNumeros = cadenaNumeros.substring(0, cadenaNumeros.length() - 1);
            }
            //se muestra en el label
            etiquetaNumeros.setText(cadenaNumeros);
        }
    }//GEN-LAST:event_botonCEActionPerformed

